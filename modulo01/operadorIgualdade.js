// Operadores de Igualdade: 
//== (Igual a),
//!= (Diferente de),
//=== (Igual e do mesmo tipo), 
//!== (Diferente inclusive do tipo)

const a = 5 //Number 
const b = '5' //String 

console.log( a == b) //true
console.log( a === b) //false
console.log( a != b) //false 
console.log( a !== b) //true








